# ─── Argument Parsing ─────────────────────────────────────────────────────────

def parse_args():
    p = argparse.ArgumentParser(description="Mini Caribou — WSL HPC Benchmark Manager",
                                formatter_class=argparse.RawTextHelpFormatter)
    p.add_argument("--slurm",      choices=["preprocessing"], metavar="MODE")
    p.add_argument("--apps",       choices=["stream", "hpl", "hpcg"])
    p.add_argument("--action",     choices=["build", "run", "all"], default="all")
    p.add_argument("--threads",    type=int, nargs="+", default=[1],  metavar="T")
    p.add_argument("--np",         type=int, nargs="+", default=None, metavar="N")
    p.add_argument("--iterations", type=int, nargs="+", default=[1],  metavar="N",
                   help="--iterations 3 → runs iters 1,2,3")
    p.add_argument("--compiler",   choices=["gcc", "aocc", "icc"], default=None)
    p.add_argument("--skip-preprocess", action="store_true")
    return p.parse_args()


# ─── Entry Point ──────────────────────────────────────────────────────────────

def main():
    args    = parse_args()
    # FIX 5: call get_system_info() once here; reuse result everywhere in main()
    sysinfo = get_system_info()

    if args.slurm == "preprocessing":
        print("=" * 64)
        print(f"  Host: {sysinfo['host']}  Cores: {sysinfo['cores']}"
              f"  RAM: {sysinfo['mem_mb']} MB total / {sysinfo['avail_mb']} MB avail")
        print("=" * 64)
        preprocess_slurm()
        return

    if not args.apps:
        print("  ✗  --apps <stream|hpl|hpcg>  or  --slurm preprocessing  is required.")
        sys.exit(1)

    workspace  = os.path.join(BASE_DIR, "workspace")
    np_list    = args.np or [sysinfo["cores"]]   # FIX 5: reuse cores from sysinfo, no second nproc call
    # Single value N → expand to [1..N]; multiple values → use as-is
    iters_list = list(range(1, args.iterations[0] + 1)) if len(args.iterations) == 1 else args.iterations

    print("=" * 64)
    print(f"  App: {args.apps.upper()}  Action: {args.action}  "
          f"Threads: {args.threads}  NP: {np_list}  Iters: {iters_list}")
    print(f"  Workspace: {workspace}")
    print("=" * 64)

    # Compiler: forced via --compiler flag, or auto-detected
    if args.compiler:
        compiler_name, ci = args.compiler, COMPILERS[args.compiler]
        if not args.skip_preprocess: preprocess_compiler_forced(compiler_name, ci)
    else:
        if not args.skip_preprocess:
            compiler_name, ci = preprocess_compiler()
        else:
            compiler_name, ci = detect_compiler()

    print(f"  ✔  Compiler : {compiler_name.upper()}  ({ci['cc']})")
    os.makedirs(workspace, exist_ok=True)

    run_pipeline(app=args.apps, threads_list=args.threads, np_list=np_list,
                 iterations_list=iters_list, action=args.action,
                 workspace=workspace, compiler_name=compiler_name, ci=ci)


if __name__ == "__main__":
    main()
