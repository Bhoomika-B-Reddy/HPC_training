def run_one_case(app, threads, np, iteration, action, workspace,
                 compiler_name, compiler_info, system_info,
                 profiling=False, metrics=None, profile_duration=10):
    results   = []
    build_dir = os.path.join(workspace, app, "build")
    run_dir   = os.path.join(workspace, app, "run",
                             f"iter{iteration}_t{threads}_np{np}")
    os.makedirs(build_dir, exist_ok=True)
    os.makedirs(run_dir,   exist_ok=True)

    system_info["compiler_name"] = compiler_name

    build_jid = None
    binary    = None

    # ── BUILD ──────────────────────────────────────────────
    if action in ("build", "all"):

        temp_b_out = os.path.join(build_dir, f"{app}.out")
        temp_b_err = os.path.join(build_dir, f"{app}.err")

        if app == "stream":
            build_jid = stream_app.build(
                build_dir, threads, compiler_info, system_info,
                out_log=temp_b_out, err_log=temp_b_err,
                workspace=workspace)
        elif app == "hpl":
            build_jid, binary = hpl_app.build(
                build_dir, threads, compiler_info, compiler_name,
                out_log=temp_b_out, err_log=temp_b_err,
                workspace=workspace)
        elif app == "hpcg":                                         # ← CHANGE 2
            build_jid, binary = hpcg_app.build(
                build_dir, threads, compiler_info, compiler_name,
                out_log=temp_b_out, err_log=temp_b_err,
                workspace=workspace)

        log_dir, b_out, b_err, b_sbatch, b_case = make_job_logdir(
            workspace, app, job_type="build")

        if build_jid and build_jid not in ("skip", "bash"):
            wait_and_show(build_jid, temp_b_out, f"build_{app}")

        for src, dst in [(temp_b_out, b_out), (temp_b_err, b_err)]:
            if os.path.exists(src):
                shutil.move(src, dst)

        write_case_setup(b_case, app, "build", compiler_name, compiler_info,
                         threads, np, iteration, system_info)

        sbatch_src = os.path.join(build_dir,
            f"build_hpl_{compiler_name}.sh"  if app == "hpl"  else
            f"build_hpcg_{compiler_name}.sh" if app == "hpcg" else
            "build_stream.sh")
        if os.path.exists(sbatch_src):
            shutil.copy(sbatch_src, b_sbatch)

        print(f"  ✔  BUILD  →  {log_dir}")

        b_ok = job_succeeded(build_jid, b_out, app=app, job_type="build")

        if build_jid not in ("skip", "bash", None):
            results.append({
                "app":       app,
                "compiler":  compiler_name,
                "job_type":  "build",
                "iteration": iteration,
                "threads":   threads,
                "np":        np,
                "status":    "PASS" if b_ok else "FAIL",
                "log_dir":   log_dir,
                "job_id":    build_jid,
                "gflops":    None,
                "triad":     None,
            })

        if not b_ok:
            print(f"\n  ✗  Build failed — skipping run for"
                  f" iter={iteration} threads={threads} np={np}")
            return results

    # ── RUN ────────────────────────────────────────────────
    if action in ("run", "all"):

        temp_out = os.path.join(run_dir, f"{app}.out")
        temp_err = os.path.join(run_dir, f"{app}.err")

        if binary is None and app == "hpl":
            binary = os.path.join(
                build_dir, "hpl-2.3", "bin",
                f"Linux_HPL_{compiler_name}", "xhpl")
        if binary is None and app == "hpcg":                        # ← CHANGE 3
            binary = os.path.join(build_dir, "xhpcg")

        # ── PROFILING MODE: direct mpirun, uProf profiles system-wide ──
        if profiling and app == "hpl":

            direct_np = np

            proc, direct_out, out_f, err_f = hpl_app.run_direct(
                build_dir, run_dir, direct_np, threads, binary,
                compiler_info, system_info,
                out_log=temp_out, err_log=temp_err)

            WARMUP_SECS = 5
            print(f"  Waiting {WARMUP_SECS}s for HPL to warm up ...")
            time.sleep(WARMUP_SECS)

            uprof.run_uprof(
                benchmark = app.upper(),
                metrics   = metrics or uprof.DEFAULT_METRICS,
                duration  = profile_duration,
            )

            print("  Waiting for HPL to finish ...")
            proc.wait()
            out_f.close()
            err_f.close()
            print("  ✔  HPL finished.")

            run_jid = f"direct_{iteration}_t{threads}_np{direct_np}"
            r_out = temp_out
            r_err = temp_err
            if os.path.getsize(r_out) == 0:
                for candidate in [
                    os.path.join(run_dir, "hpl.out"),
                    os.path.join(os.path.dirname(run_dir), "hpl.out"),
                ]:
                    if candidate != r_out and os.path.exists(candidate) \
                            and os.path.getsize(candidate) > 0:
                        shutil.copy(candidate, r_out)
                        print(f"  [debug] Copied HPL output from {candidate}")
                        break

        # ── NORMAL MODE: SLURM ─────────────────────────────────────────
        else:
            if app == "stream":
                run_jid, _ = stream_app.run(
                    build_dir, run_dir, threads, build_jid, compiler_info,
                    out_log=temp_out, err_log=temp_err,
                    workspace=workspace)
            elif app == "hpl":
                run_jid, _ = hpl_app.run(
                    build_dir, run_dir, np, threads, build_jid,
                    binary, compiler_info, system_info,
                    out_log=temp_out, err_log=temp_err,
                    workspace=workspace)
            elif app == "hpcg":                                     # ← CHANGE 4
                run_jid, _ = hpcg_app.run(
                    build_dir, run_dir, np, threads, build_jid,
                    binary, compiler_info, system_info,
                    out_log=temp_out, err_log=temp_err,
                    workspace=workspace)

            if run_jid and run_jid not in ("bash",):
                wait_and_show(run_jid, None, f"run_{app}")

            r_out = temp_out
            r_err = temp_err

        # ── LOG HANDLING (common to both paths) ────────────────────────
        log_dir, final_out, final_err, r_sbatch, r_case = make_job_logdir(
            workspace, app, job_type="run")

        for src, dst in [(r_out, final_out), (r_err, final_err)]:
            if os.path.exists(src):
                shutil.move(src, dst)

        write_case_setup(r_case, app, "run", compiler_name, compiler_info,
                         threads, np, iteration, system_info)

        sbatch_src = os.path.join(run_dir, f"run_{app}.sh")
        if os.path.exists(sbatch_src):
            shutil.copy(sbatch_src, r_sbatch)

        if app == "stream":
            print_stream_results(final_out)

        r_ok   = job_succeeded(run_jid, final_out, app=app, job_type="run")
        gflops = (extract_gflops(final_out)      if app == "hpl"    else
                  extract_gflops_hpcg(final_out) if app == "hpcg"   else None)
        triad  = extract_triad(final_out)        if app == "stream" else None

        print(f"  ✔  RUN    →  {log_dir}")

        results.append({
            "app":       app,
            "compiler":  compiler_name,
            "job_type":  "run",
            "iteration": iteration,
            "threads":   threads,
            "np":        np,
            "status":    "PASS" if r_ok else "FAIL",
            "log_dir":   log_dir,
            "job_id":    run_jid,
            "gflops":    gflops,
            "triad":     triad,
        })

    return results


