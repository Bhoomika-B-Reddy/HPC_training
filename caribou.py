# MAIN PIPELINE  (sweeps iterations × threads × np)
# ─────────────────────────────────────────────────────────────

def run_pipeline(app, threads_list, np_list, iterations_list, action, workspace,
                 compiler_name, compiler_info,
                 profiling=False, metrics=None, profile_duration=10):
    system_info = get_system_info()
    all_results = []

    write_lmod_module(app, compiler_name, compiler_info, workspace)

    for iteration in iterations_list:
        for threads in threads_list:
            for np in np_list:
                case_results = run_one_case(
                    app, threads, np, iteration, action, workspace,
                    compiler_name, compiler_info, dict(system_info),
                    profiling=profiling,
                    metrics=metrics,
                    profile_duration=profile_duration,
                )
                all_results.extend(case_results)

    print_summary_table(all_results)

    summary_path = os.path.join(workspace, app, "logs", "summary.json")
    os.makedirs(os.path.dirname(summary_path), exist_ok=True)
    with open(summary_path, "w") as f:
        json.dump(all_results, f, indent=2)
    print(f"  Summary JSON : {summary_path}")

    return all_results


