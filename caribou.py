def write_lmod_module(app, compiler_name, compiler_info, workspace):
    mod_dir  = os.path.join(workspace, "modules", app, compiler_name)
    os.makedirs(mod_dir, exist_ok=True)
    mod_file = os.path.join(mod_dir, "1.0.lua")

    build_dir = os.path.join(workspace, app, "build")
    run_dir   = os.path.join(workspace, app, "run")

    lua = f"""\
-- Lmod modulefile for {app.upper()} built with {compiler_name.upper()}
-- Auto-generated by Mini Caribou
whatis("Name        : {app.upper()}")
whatis("Version     : 1.0")
whatis("Compiler    : {compiler_name.upper()}")
whatis("CC          : {compiler_info['cc']}")
whatis("Flags       : {compiler_info['flags']}")
whatis("Build dir   : {build_dir}")
whatis("Run dir     : {run_dir}")

help([[
  {app.upper()} benchmark built with {compiler_name.upper()}.
  Build directory : {build_dir}
  Run   directory : {run_dir}
  Load with       : module load {app}/{compiler_name}/1.0
]])

local build = "{build_dir}"
local run   = "{run_dir}"

prepend_path("PATH",         build)
prepend_path("PATH",         run)
setenv("CARIBOU_{app.upper()}_BUILD", build)
setenv("CARIBOU_{app.upper()}_RUN",   run)
setenv("CARIBOU_COMPILER",            "{compiler_name.upper()}")
setenv("CARIBOU_CC",                  "{compiler_info['cc']}")
"""
    with open(mod_file, "w") as f:
        f.write(lua)

    print(f"     Lmod module written : {mod_file}")
    print(f"     Load with : module use {os.path.join(workspace, 'modules')}")
    print(f"                 module load {app}/{compiler_name}/1.0")
    return mod_file


