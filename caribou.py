# ARG PARSE
# ─────────────────────────────────────────────────────────────

def parse_args():
    p = argparse.ArgumentParser(
        description="Mini Caribou — WSL HPC Benchmark Manager",
        formatter_class=argparse.RawTextHelpFormatter)

    p.add_argument("--slurm",
        choices=["preprocessing"],
        metavar="MODE",
        help=(
            "SLURM operation to run standalone.\n"
            "  preprocessing  — check tools, start munge/daemons, verify sinfo"
        ))

    p.add_argument("--apps",
        choices=["stream", "hpl", "hpcg"],       # ← CHANGE 5: hpcg added
        help="Benchmark app to run")
    p.add_argument("--action",
        choices=["build", "run", "all"], default="all",
        help="Pipeline stage (default: all)")
    p.add_argument("--threads",
        type=int, nargs="+", default=[1], metavar="T",
        help="OMP thread counts to sweep. e.g. --threads 1 2 4\nDefault: 1")
    p.add_argument("--np",
        type=int, nargs="+", default=None, metavar="N",
        help="MPI rank counts to sweep. e.g. --np 1 2 4\nDefault: system core count")
    p.add_argument("--iterations",
        type=int, nargs="+", default=[1], metavar="N",
        help="--iterations 3 → runs iter 1,2,3\nDefault: 1")
    p.add_argument("--compiler",
        choices=["gcc", "aocc", "icc"], default=None,
        help="Force specific compiler (default: auto-detect)")
    p.add_argument("--skip-preprocess",
        action="store_true",
        help="Skip compiler and SLURM preprocessing")

    # uProf profiling args
    uprof.add_uprof_args(p)

    return p.parse_args()


# ─────────────────────────────────────────────────────────────
# ENTRY POINT
# ─────────────────────────────────────────────────────────────

def main():
    args = parse_args()

    if args.slurm == "preprocessing":
        run_slurm_preprocessing()
        return

    if not args.apps:
        print("  ✗  Either --slurm <mode> or --apps <app> is required.")
        print("      python3 caribou.py --slurm preprocessing")
        print("      python3 caribou.py --apps stream --action all")
        sys.exit(1)

    workspace = os.path.join(BASE_DIR, "workspace")

    nproc        = int(subprocess.run(
        ["nproc"], capture_output=True, text=True).stdout.strip())
    threads_list = args.threads
    np_list      = args.np if args.np else [nproc]

    raw_iters  = args.iterations
    iters_list = list(range(1, raw_iters[0] + 1)) if len(raw_iters) == 1 else raw_iters

    print("=" * 64)
    print("     Mini Caribou  —  WSL HPC Benchmark Manager")
    print("=" * 64)
    print(f"  App        : {args.apps.upper()}")
    print(f"  Action     : {args.action}")
    print(f"  Threads    : {threads_list}")
    print(f"  NP         : {np_list}")
    print(f"  Iterations : {iters_list}")
    print(f"  Profiling  : {'ON — ' + ', '.join(args.metrics) if args.profiling else 'OFF (SLURM)'}")
    print(f"  Workspace  : {workspace}")
    print("=" * 64)

    if args.profiling:
        uprof.check_uprof()

    if args.compiler:
        compiler_name = args.compiler
        compiler_info = COMPILERS[args.compiler]
        if not args.skip_preprocess:
            preprocess_compiler_forced(compiler_name, compiler_info)
        print(f"  ✔  Compiler : {compiler_name.upper()}  ({compiler_info['cc']})")
    else:
        if not args.skip_preprocess:
            compiler_name, compiler_info = preprocess_compiler()
        else:
            compiler_name, compiler_info = detect_compiler()
            print(f"  ✔  Compiler : {compiler_name.upper()}  ({compiler_info['cc']})")

    os.makedirs(workspace, exist_ok=True)

    run_pipeline(
        app=args.apps,
        threads_list=threads_list,
        np_list=np_list,
        iterations_list=iters_list,
        action=args.action,
        workspace=workspace,
        compiler_name=compiler_name,
        compiler_info=compiler_info,
        profiling=args.profiling,
        metrics=args.metrics,
        profile_duration=args.profile_duration,
    )


if __name__ == "__main__":
    main()
