def extract_gflops(log_path):
    """Extract Gflops from HPL output (WR... line)."""
    if not log_path or not os.path.exists(log_path):
        return None
    with open(log_path) as f:
        for line in f:
            stripped = line.strip()
            if stripped.startswith("WR"):
                parts = stripped.split()
                if len(parts) >= 7:
                    try:
                        return float(parts[6])
                    except ValueError:
                        pass
    return None


def extract_gflops_hpcg(log_path):
    """Extract Gflops from HPCG output (HPCG_result line)."""
    if not log_path or not os.path.exists(log_path):
        return None
    with open(log_path) as f:
        for line in f:
            stripped = line.strip()
            if stripped.startswith("HPCG_result"):
                # format: HPCG_result : iters=N  residual=X  GFlops=Y
                for part in stripped.split():
                    if part.startswith("GFlops="):
                        try:
                            return float(part.split("=")[1])
                        except ValueError:
                            pass
    return None


def extract_triad(log_path):
    """Extract Triad bandwidth (MB/s) from STREAM output."""
    if not log_path or not os.path.exists(log_path):
        return None
    with open(log_path) as f:
        for line in f:
            stripped = line.strip()
            if stripped.startswith("Triad:"):
                parts = stripped.split()
                if len(parts) >= 2:
                    try:
                        return float(parts[1])
                    except ValueError:
                        pass
    return None


def print_stream_results(log_path):
    """Print the STREAM bandwidth table to terminal after run completes."""
    if not log_path or not os.path.exists(log_path):
        return
    with open(log_path) as f:
        lines = f.readlines()
    sep_indices = [i for i, l in enumerate(lines) if "-----" in l]
    if len(sep_indices) < 2:
        return
    print()
    print("  [STREAM Results]")
    for line in lines[sep_indices[0]:sep_indices[1] + 1]:
        print("  " + line.rstrip())
    print()


def job_succeeded_slurm(job_id):
    try:
        r = subprocess.run(
            ["sacct", "-j", str(job_id),
             "--format=JobID,State,ExitCode",
             "--noheader", "-P"],
            capture_output=True, text=True, timeout=10)
        if r.returncode != 0 or not r.stdout.strip():
            return None, "sacct returned nothing"
        first_line = r.stdout.strip().splitlines()[0]
        parts      = first_line.split("|")
        if len(parts) >= 2:
            state    = parts[1].strip().upper()
            exitcode = parts[2].strip() if len(parts) > 2 else "?"
            if state in ("COMPLETED", "COMPLETING"):
                return True,  f"SLURM state={state} exit={exitcode}"
            elif state in ("FAILED", "CANCELLED", "TIMEOUT", "NODE_FAIL"):
                return False, f"SLURM state={state} exit={exitcode}"
            else:
                return None,  f"SLURM state={state} (inconclusive)"
    except FileNotFoundError:
        return None, "sacct not available"
    except Exception as e:
        return None, f"sacct exception: {e}"
    return None, "sacct parse failed"


def job_succeeded(job_id, out_log, app="stream", job_type="run"):
    if job_id == "skip":
        return True
    if job_id in ("bash", None) or str(job_id).startswith("direct_"):
        ok, _ = check_log_for_success(out_log, app, job_type)
        return ok
    slurm_ok, _ = job_succeeded_slurm(job_id)
    if slurm_ok is not None:
        if slurm_ok:
            log_ok, _ = check_log_for_success(out_log, app, job_type)
            if not log_ok:
                return False
        return slurm_ok
    ok, _ = check_log_for_success(out_log, app, job_type)
    return ok


