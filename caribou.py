def get_system_info():
    cores = int(subprocess.run(
        ["nproc"], capture_output=True, text=True).stdout.strip())
    host  = subprocess.run(
        ["hostname"], capture_output=True, text=True).stdout.strip()
    mem_mb, avail_mb = 4096, 1024
    try:
        with open("/proc/meminfo") as f:
            for line in f:
                if line.startswith("MemTotal"):
                    mem_mb = int(line.split()[1]) // 1024
                if line.startswith("MemAvailable"):
                    avail_mb = int(line.split()[1]) // 1024
    except:
        pass
    return {"cores": cores, "host": host,
            "mem_mb": mem_mb, "avail_mb": avail_mb}


