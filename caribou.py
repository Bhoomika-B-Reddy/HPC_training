SUCCESS_MARKERS = {
    "stream": ["Triad:", "Results saved to"],
    "hpl":    ["WR", "HPL_result", "Results saved to"],
    "hpcg":   ["HPCG_result"],
    "build":  ["Binary OK", "Done :"],
}

FAIL_MARKERS = [
    "ERROR:", "error:", "compile failed", "xhpl not found",
    "exit 1", "No such file or directory", "Segmentation fault",
    "command not found", "No such file",
]


def check_log_for_success(log_path, app, job_type):
    if not log_path or not os.path.exists(log_path):
        return False, "log file not found"
    with open(log_path) as f:
        content = f.read()
    for marker in FAIL_MARKERS:
        if marker in content:
            return False, f"fail marker: '{marker}'"
    key     = "build" if job_type == "build" else app
    markers = SUCCESS_MARKERS.get(key, ["Done :"])
    for marker in markers:
        if marker in content:
            return True, f"success marker: '{marker}'"
    if len(content.strip()) > 0:
        return False, "no success marker in log"
    return False, "log is empty"


