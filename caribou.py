def make_job_logdir(workspace, app, job_type="run"):
    """Name: case_YYYYMMDD_HHMMSS_{job_type}_{app}"""
    ts      = time.strftime("%Y%m%d_%H%M%S")
    folder  = f"case_{ts}_{job_type}_{app}"
    log_dir = os.path.join(workspace, app, "logs", folder)
    os.makedirs(log_dir, exist_ok=True)

    out_log    = os.path.join(log_dir, f"{app}.out")
    err_log    = os.path.join(log_dir, f"{app}.err")
    sbatch_cp  = os.path.join(log_dir, f"{app}.sbatch")
    case_setup = os.path.join(log_dir, "case_setup.json")

    return log_dir, out_log, err_log, sbatch_cp, case_setup


def write_case_setup(path, app, job_type, compiler_name, compiler_info,
                     threads, np, iteration, system_info, extra=None):
    data = {
        "app":          app,
        "job_type":     job_type,
        "compiler":     compiler_name,
        "cc":           compiler_info["cc"],
        "flags":        compiler_info["flags"],
        "threads":      threads,
        "np":           np,
        "iteration":    iteration,
        "host":         system_info.get("host", "?"),
        "cores_system": system_info.get("cores", "?"),
        "mem_mb":       system_info.get("mem_mb", "?"),
        "avail_mb":     system_info.get("avail_mb", "?"),
        "timestamp":    time.strftime("%Y-%m-%dT%H:%M:%S"),
    }
    if extra:
        data.update(extra)
    with open(path, "w") as f:
        json.dump(data, f, indent=2)
    return data


