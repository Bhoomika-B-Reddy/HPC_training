def print_summary_table(results):
    if not results:
        print("\n  (no results to show)")
        return

    # Per-app averages over run jobs only
    buckets = defaultdict(list)
    for r in results:
        if r.get("job_type") != "run":
            continue
        if r["app"] == "hpl"    and r.get("gflops") is not None:
            buckets["hpl"].append(r["gflops"])
        if r["app"] == "hpcg"   and r.get("gflops") is not None:
            buckets["hpcg"].append(r["gflops"])
        if r["app"] == "stream" and r.get("triad")  is not None:
            buckets["stream"].append(r["triad"])
    avg_map = {app: sum(v) / len(v) for app, v in buckets.items() if v}

    # Detect which apps are present to decide metric columns
    apps_present = {r["app"] for r in results}
    show_gflops  = bool(apps_present & {"hpl", "hpcg"})
    show_triad   = "stream" in apps_present

    W = {"app": 8, "compiler": 9, "job_type": 12,
         "iter": 5, "threads": 8, "np": 4, "status": 9, "log": 30}
    if show_gflops:
        W["gflops"]     = 12
        W["avg_gflops"] = 12
    if show_triad:
        W["triad"]      = 12
        W["avg_triad"]  = 12

    def cell(val, width, right=False):
        s = str(val)
        return s.rjust(width) if right else s.ljust(width)

    def hdr():
        h = (f"  │ {cell('App',W['app'])} │ {cell('Compiler',W['compiler'])} │"
             f" {cell('Job Type',W['job_type'])} │ {cell('Iter',W['iter'])} │"
             f" {cell('Threads',W['threads'])} │ {cell('NP',W['np'])} │"
             f" {cell('Status',W['status'])}")
        if show_gflops:
            h += f" │ {cell('Gflops',W['gflops'],True)} │ {cell('Avg Gflops',W['avg_gflops'],True)}"
        if show_triad:
            h += f" │ {cell('Triad MB/s',W['triad'],True)} │ {cell('Avg Triad',W['avg_triad'],True)}"
        h += f" │ {cell('Log Dir',W['log'])} │"
        return h

    def data_row(r):
        ok        = r["status"] == "PASS"
        sym       = "✔" if ok else "✗"
        s         = f"{sym} {r['status']}"
        short_log = os.path.basename(r.get("log_dir", "-")) if r.get("log_dir") else "-"

        row = (f"  │ {cell(r['app'],W['app'])} │ {cell(r['compiler'],W['compiler'])} │"
               f" {cell(r['job_type'],W['job_type'])} │ {cell(r.get('iteration','-'),W['iter'])} │"
               f" {cell(r.get('threads','-'),W['threads'])} │ {cell(r.get('np','-'),W['np'])} │"
               f" {cell(s,W['status'])}")

        if show_gflops:
            if r["app"] in ("hpl", "hpcg") and r.get("job_type") == "run":
                gf     = f"{r['gflops']:.4e}" if r.get("gflops") is not None else "-"
                avg_gf = f"{avg_map[r['app']]:.4e}" if r["app"] in avg_map else "-"
            else:
                gf = avg_gf = "-"
            row += f" │ {cell(gf,W['gflops'],True)} │ {cell(avg_gf,W['avg_gflops'],True)}"

        if show_triad:
            if r["app"] == "stream" and r.get("job_type") == "run":
                tr     = f"{r['triad']:.2f}" if r.get("triad") is not None else "-"
                avg_tr = f"{avg_map['stream']:.2f}" if "stream" in avg_map else "-"
            else:
                tr = avg_tr = "-"
            row += f" │ {cell(tr,W['triad'],True)} │ {cell(avg_tr,W['avg_triad'],True)}"

        row += f" │ {cell(short_log,W['log'])} │"
        return row

    w = sum(W.values()) + 3 * len(W) + 1
    border_top = "  ┌" + "─" * (w - 4) + "┐"
    border_mid = "  ├" + "─" * (w - 4) + "┤"
    border_bot = "  └" + "─" * (w - 4) + "┘"

    print()
    print("  ╔══════════════════════════════════════╗")
    print("  ║   CARIBOU  —  POST-PROCESSING REPORT ║")
    print("  ╚══════════════════════════════════════╝")
    print()
    print(border_top)
    print(hdr())
    print(border_mid)

    pass_count = fail_count = 0
    for r in results:
        print(data_row(r))
        if r["status"] == "PASS":
            pass_count += 1
        else:
            fail_count += 1

    print(border_bot)
    print()
    total = pass_count + fail_count
    print(f"  Summary: {total} jobs  │  ✔ PASS: {pass_count}  │  ✗ FAIL: {fail_count}")
    if "hpl"    in avg_map:
        print(f"  Avg Gflops (HPL)   : {avg_map['hpl']:.4e}  (over {len(buckets['hpl'])} run(s))")
    if "hpcg"   in avg_map:
        print(f"  Avg Gflops (HPCG)  : {avg_map['hpcg']:.4e}  (over {len(buckets['hpcg'])} run(s))")
    if "stream" in avg_map:
        print(f"  Avg Triad MB/s     : {avg_map['stream']:.2f}  (over {len(buckets['stream'])} run(s))")

    failures = [r for r in results if r["status"] == "FAIL"]
    if failures:
        print()
        print("  ✗ FAILED JOBS:")
        for r in failures:
            print(f"    • {r['app']} / {r['job_type']}"
                  f"  iter={r.get('iteration', '-')}"
                  f"  threads={r.get('threads', '-')}"
                  f"  np={r.get('np', '-')}"
                  f"  jobid={r.get('job_id', '-')}")
            if r.get("log_dir"):
                print(f"      Log dir : {r['log_dir']}")
    print()


